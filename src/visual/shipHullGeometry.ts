const geometryCache = new Map<string, ShipTierGeometry>();

/**
 * Resolve the geometry for a hull family at an evolution tier (0..4).
 * Unknown hull ids fall back to the default dart so a stale save can never
 * render nothing. Results are cached (pure data, safe to share).
 */
export function getShipTierGeometry(hullId: string, tierIndex: number): ShipTierGeometry {
  const resolvedHullId: ShipHullId =
    (SHIP_HULL_IDS as readonly string[]).includes(hullId) ? (hullId as ShipHullId) : DEFAULT_HULL_ID;
  const clampedTier = Math.max(0, Math.min(SHIP_TIER_COUNT - 1, Math.floor(tierIndex)));

  const cacheKey = `${resolvedHullId}:${clampedTier}`;
  let geometry = geometryCache.get(cacheKey);
  if (!geometry) {
    geometry = finalizeGeometry(HULL_BUILDERS[resolvedHullId](clampedTier), clampedTier);
    geometryCache.set(cacheKey, geometry);
  }
  return geometry;
}

/** Axis-aligned bounds of an outline (preview scaling, cache-fit tests). */
export function getHullBounds(outline: Point2D[]): {
  minX: number; maxX: number; minY: number; maxY: number;
} {
  let minX = Infinity, maxX = -Infinity, minY = Infinity, maxY = -Infinity;
  for (const point of outline) {
    if (point.x < minX) minX = point.x;
    if (point.x > maxX) maxX = point.x;
    if (point.y < minY) minY = point.y;
    if (point.y > maxY) maxY = point.y;
  }
  return { minX, maxX, minY, maxY };
}
