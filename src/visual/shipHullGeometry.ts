/** Overall size multiplier per evolution tier. */
const TIER_SCALE = [1.0, 1.16, 1.32, 1.48, 1.62];

const P = (x: number, y: number): Point2D => ({ x, y });

/**
 * Frame authored per hull per tier — everything else (mirroring, traces,
 * channels, edge accents) is derived from it.
 */
interface HullFrame {
  nose: Point2D;
  /** Upper half outline, nose → tail, all y < 0. Mirrored automatically. */
  upperHull: Point2D[];
  /** Optional centerline tail point (omit for a flat/open notched tail). */
  tailCenter?: Point2D;
  cockpit: Point2D[];
  /** Full engine set (author ± pairs explicitly via enginePair). */
  engines: Point2D[];
  engineNozzleRadius: number;
  /** Upper-half accent anchors; mirrored automatically. */
  wingTips: Point2D[];
  wingTipAccentRadius: number;
  /** Upper-half pod anchors; mirrored automatically. */
  pods: Point2D[];
  energyPodRadius: number;
}

/** Mirror upper-half anchors across the x axis (y=0 anchors stay single). */
function mirrorAnchors(upper: Point2D[]): Point2D[] {
  const result: Point2D[] = [];
  for (const point of upper) {
    result.push(point);
    if (point.y !== 0) result.push(P(point.x, -point.y));
  }
  return result;
}

/** Symmetric engine pair helper. */
function enginePair(x: number, y: number): Point2D[] {
  return [P(x, -y), P(x, y)];
}

/** Six-point hex cockpit (front point → upper edge → back point → lower edge). */
function hexCockpit(frontX: number, backX: number, halfWidth: number): Point2D[] {
  const length = frontX - backX;
  const upperFrontX = backX + length * 0.65;
  const upperBackX = backX + length * 0.22;
  return [
    P(frontX, 0),
    P(upperFrontX, -halfWidth),
    P(upperBackX, -halfWidth),
    P(backX, 0),
    P(upperBackX, halfWidth),
    P(upperFrontX, halfWidth),
  ];
}

/** Four-point diamond cockpit. */
function diamondCockpit(frontX: number, backX: number, halfWidth: number): Point2D[] {
  const midX = (frontX + backX) / 2;
  return [P(frontX, 0), P(midX, -halfWidth), P(backX, 0), P(midX, halfWidth)];
}

/** Wide low trapezoid visor (heavy ships — bridge slit, not a bubble). */
function visorCockpit(frontX: number, backX: number, halfWidth: number): Point2D[] {
  const length = frontX - backX;
  return [
    P(frontX, -halfWidth * 0.45),
    P(backX + length * 0.15, -halfWidth),
    P(backX, -halfWidth * 0.55),
    P(backX, halfWidth * 0.55),
    P(backX + length * 0.15, halfWidth),
    P(frontX, halfWidth * 0.45),
  ];
}

// ---------------------------------------------------------------------------
// Hull family builders — one per playable ship archetype
// ---------------------------------------------------------------------------
//
// Each builder receives the tier index (0..4) and returns a HullFrame.
// `k` scales the whole craft per tier; `w` widens wings faster than the hull
// so higher tiers read more aggressive, not just bigger. Vertices gated on
// tier grow new blades/facets at evolution breakpoints.

type HullBuilder = (tier: number) => HullFrame;

const buildDart: HullBuilder = (t) => {
  const k = TIER_SCALE[t];
  const w = 1 + t * 0.08;
  const p = (x: number, y: number) => P(x * k, y * k);
  const wing = (x: number, y: number) => P(x * k, y * k * w);

  const upperHull: Point2D[] = [p(13, -2.6), p(4, -4.6), p(-3, -5)];
  if (t >= 2) upperHull.push(wing(-7, -8));           // mid-wing facet
  upperHull.push(wing(-11, -10.5));                   // main swept tip
  if (t >= 4) upperHull.push(wing(-15.5, -12.5));     // apex blade extension
  upperHull.push(p(-8, -5), p(-12, -2.4));            // tail notch fin

  const engines =
    t === 0 ? [P(-7 * k, 0)] :
    t === 1 ? enginePair(-7.5 * k, 2.4 * k) :
    t === 2 ? [P(-8 * k, 0), ...enginePair(-8 * k, 3.2 * k)] :
    t === 3 ? [P(-8.5 * k, 0), ...enginePair(-8.5 * k, 3.6 * k)] :
    [P(-9 * k, 0), ...enginePair(-9 * k, 3.6 * k), ...enginePair(-8 * k, 1.8 * k)];

  return {
    nose: p(22, 0),
    upperHull,
    tailCenter: p(-9, 0),
    cockpit: hexCockpit(12 * k, -1 * k, 2.1 * k),
    engines,
    engineNozzleRadius: (1.9 + t * 0.25) * k * 0.9,
    wingTips: [t >= 4 ? wing(-15.5, -12.5) : wing(-11, -10.5)],
    wingTipAccentRadius: 1.0 + t * 0.3,
    pods: t >= 3 ? [wing(-5, -6.8)] : [],
    energyPodRadius: 1.8 + (t - 3) * 0.5,
  };
};

