function buildOutline(frame: HullFrame): Point2D[] {
  const lower = frame.upperHull
    .slice()
    .reverse()
    .map((point) => P(point.x, -point.y));
  return frame.tailCenter
    ? [frame.nose, ...frame.upperHull, frame.tailCenter, ...lower]
    : [frame.nose, ...frame.upperHull, ...lower];
}

function cockpitBounds(cockpit: Point2D[]): { frontX: number; backX: number; halfWidth: number } {
  let frontX = -Infinity;
  let backX = Infinity;
  let halfWidth = 0;
  for (const point of cockpit) {
    if (point.x > frontX) frontX = point.x;
    if (point.x < backX) backX = point.x;
    const absY = Math.abs(point.y);
    if (absY > halfWidth) halfWidth = absY;
  }
  return { frontX, backX, halfWidth };
}

function buildEnergyChannels(frame: HullFrame, tier: number): EnergyChannel[] {
  const channels: EnergyChannel[] = [];
  const { frontX, backX } = cockpitBounds(frame.cockpit);

  // Central spine: cockpit → rearmost engine (or tail).
  let rearX = frame.tailCenter ? frame.tailCenter.x * 0.9 : backX - 4;
  for (const engine of frame.engines) {
    if (engine.x < rearX) rearX = engine.x;
  }
  channels.push({
    path: [P(frontX * 0.85, 0), P(rearX, 0)],
    width: 1.2 + tier * 0.08,
    baseAlpha: 0.5 + tier * 0.025,
  });

  // Side conduits: cockpit rear → each off-axis engine (dedup mirrored pairs).
  const seen = new Set<string>();
  for (const engine of frame.engines) {
    if (engine.y === 0) continue;
    const key = `${engine.x.toFixed(2)}:${Math.abs(engine.y).toFixed(2)}`;
    if (seen.has(key)) continue;
    seen.add(key);
    const sign = engine.y < 0 ? -1 : 1;
    for (const side of [sign, -sign]) {
      channels.push({
        path: [
          P(backX, 0),
          P((backX + engine.x) / 2, Math.abs(engine.y) * 0.7 * side),
          P(engine.x, Math.abs(engine.y) * side),
        ],
        width: 1.0,
        baseAlpha: 0.45,
      });
    }
    if (channels.length >= 5) break; // spine + 2 mirrored pairs max
  }

  return channels;
}

function buildCircuitTraces(frame: HullFrame, tier: number): CircuitTrace[] {
  const traces: CircuitTrace[] = [];
  const alpha = 0.3 + tier * 0.03;

  // Wing traces: diagonal circuit lines chasing each accent anchor.
  for (const tip of frame.wingTips) {
    if (tip.y === 0) continue;
    for (const sign of [1, -1]) {
      traces.push({
        path: [
          P(tip.x * 0.2, tip.y * 0.3 * sign),
          P(tip.x * 0.6, tip.y * 0.62 * sign),
          P(tip.x * 0.9, tip.y * 0.88 * sign),
        ],
        width: 0.6,
        alpha,
      });
    }
  }

  // Pod feed lines.
  for (const pod of frame.pods) {
    for (const sign of [1, -1]) {
      traces.push({
        path: [P(pod.x * 0.4, pod.y * 0.4 * sign), P(pod.x, pod.y * sign)],
        width: 0.7,
        alpha: 0.4,
      });
    }
  }

  // Tier 4+: parallel fuselage circuit rails.
  if (tier >= 3) {
    const { frontX, halfWidth } = cockpitBounds(frame.cockpit);
    const railY = Math.max(1.4, halfWidth * 0.8);
    const rearX = (frame.tailCenter ? frame.tailCenter.x : -frontX) * 0.6;
    for (const sign of [1, -1]) {
      traces.push({
        path: [P(frontX * 0.55, railY * sign), P(rearX, railY * sign)],
        width: 0.4,
        alpha: 0.2,
      });
    }
  }

  return traces;
}

function buildWingEdgeSegments(frame: HullFrame, tier: number): WingEdgeSegment[] {
  if (tier === 0) return [];

  // Longest upper-hull edges are the leading edges — accent their midsections.
  const vertices = [frame.nose, ...frame.upperHull];
  const edges: { from: Point2D; to: Point2D; length: number }[] = [];
  for (let i = 1; i < vertices.length; i++) {
    const from = vertices[i - 1];
    const to = vertices[i];
    const length = Math.hypot(to.x - from.x, to.y - from.y);
    edges.push({ from, to, length });
  }
  edges.sort((a, b) => b.length - a.length);

  const count = Math.min(tier, 3, edges.length);
  const segments: WingEdgeSegment[] = [];
  for (let i = 0; i < count; i++) {
    const edge = edges[i];
    for (const sign of [1, -1]) {
      // Midsection (25%..75%), pulled 8% toward the origin (inset from the rim).
      const at = (fraction: number): Point2D => P(
        (edge.from.x + (edge.to.x - edge.from.x) * fraction) * 0.92,
        (edge.from.y + (edge.to.y - edge.from.y) * fraction) * 0.92 * sign
      );
      segments.push({ from: at(0.25), to: at(0.75) });
    }
  }
  return segments;
}

function finalizeGeometry(frame: HullFrame, tier: number): ShipTierGeometry {
  return {
    hullOutline: buildOutline(frame),
    cockpit: frame.cockpit,
    engineNozzles: frame.engines,
    engineNozzleRadius: frame.engineNozzleRadius,
    wingTipAccents: mirrorAnchors(frame.wingTips),
    wingTipAccentRadius: frame.wingTipAccentRadius,
    energyPods: mirrorAnchors(frame.pods),
    energyPodRadius: frame.pods.length > 0 ? frame.energyPodRadius : 0,
    circuitTraces: buildCircuitTraces(frame, tier),
    energyChannels: buildEnergyChannels(frame, tier),
    wingEdgeSegments: buildWingEdgeSegments(frame, tier),
  };
}

